package ua.edu.ukma.Zhytnetsky;

public final class MathUtils {

    private MathUtils() {}

    public static Integer add(final Integer first, final Integer second) {
        return first + second;
    }

    public static Integer mult(final Integer first, final Integer second) {
        return first * second;
    }

    public static Integer sub(final Integer first, final Integer second) {
        return first - second;
    }

    public static Integer div(final Integer first, final Integer second) {
        return first / second;
    }

}
